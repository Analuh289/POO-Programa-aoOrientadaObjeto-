package SisVeiculo;

public class Veiculo {
// atributos
public String modelo;
public String marca;
public int ano;
public double precoDiaria;

// construtor
public Veiculo(String modelo, String marca, int ano, double precoDiaria) {
	this.modelo = modelo;
	this.marca = marca;
	this.ano = ano;
	this.precoDiaria = precoDiaria;
}
}
